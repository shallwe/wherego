def parse_cities(province):
    data = json.load(open("data/{}.json".format(province)))
    ret = {}
    for city in data['features']:
        name = city['properties']['name']
        if '市' == name[-1]:
            name = name[:-1]
        ret[name] = city['properties']['cp']
    return ret


